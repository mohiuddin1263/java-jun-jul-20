package net.timeandtraining.inheritance;

public class Human {
	

	
	public void swim() {
		System.out.println("swimming...");
	}
	
	public void sing() {
		System.out.println("sleeping...");
	}
	public void sleep() {
		System.out.println("sleeping...");
	}
	
	
	public void walk() {
		System.out.println("walking...");
	}
	
	
	public void talk() {
		System.out.println("talking...");
	}
	
	
	public void run() {
		System.out.println("running...");
	}

}
